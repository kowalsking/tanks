  public create (loader: Loader): void {
    for (let i = 0; i < this.map.length; i++) {
      for (let j = 0; j < this.map[i].length; j++) {
        const pieceName = `${this.map[i][j]}`
        let piece

        if (pieceName === '0') continue // empty
        if (!Map[this.map[i][j]]) continue 
        piece = new Sprite(loader.getTexture(Map[this.map[i][j]]))
        piece.name = Map[this.map[i][j]]
        piece.width = 36
        piece.height = 36
        piece.anchor.set(0.5)
        piece.position.set(piece.height * j + piece.height / 2, piece.width * i + piece.width / 2)
        this.addChild(piece)
        this.field.push(piece)
      }
    }
    this.tank.create(loader.getTexture('tank'))
    this.addChild(this.tank)
    
    for (let i = 0; i <= ENEMY_COUNT; i++) {
      const enemy = new Enemy()
      enemy.create(loader.getTexture('enemy_blue'), 400 + 50 * i, 500 + 70 * i)
      this.enemies.push(enemy)
      // , piece.height * j + piece.height / 2, piece.width * i + piece.width / 2
      this.addChild(enemy)
    }
  }
